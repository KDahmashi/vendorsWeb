package vendorsModel;

public class Alert {
	public String  Visible="hide";
	public String Message;
	
	public String getVisible() {
		return Visible;
	}
	public void setVisible(String visible) {
		Visible = visible;
	}
	public String getMessage() {
		return Message;
	}
	public void setMessage(String message) {
		Message = message;
	} 

}
